package exception.handling;

public class Constructor_Super_keyword {
	
	public Constructor_Super_keyword() {
		System.out.println("Default constructor");
	}
	
	public  Constructor_Super_keyword(int i) {
		System.out.println("value of int i is "+i);
	}
	public  Constructor_Super_keyword(int j,int i) {
		System.out.println("value of int i is "+i);
		System.out.println("value of int j is "+j);
	}

}
