package secondpackage;

public class New_line {
	// concept of println and print
	public static void main(String[]args) {
		System.out.println(" Hello "  +  " Mohan ");
		System.out.println(" Mohan: " + 2830);
		System.out.print("Hello");
		System.out.println("Savan");	
		System.out.println("Hello, Mohan how are you?");// println is used to show output in different line.
		System.out.print("I am fine.");
		System.out.print("where do u live?");
		System.out.print("I live in pune."); // print is used to show output in same line.
		
		System.out.println("Hello Mohan," +"\n" + "How are you");
		System.out.print("I am fine. " +"I am doing well");
		System.out.println("I love trekking\n"  + "And also love to play in raining");
		System.out.println("Pune is best place to live" + "\nSwargate is big bus stand ");
		System.out.println(123 + 456);
		
}
}
